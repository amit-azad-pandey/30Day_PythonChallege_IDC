# Challenge
# Calculate the area of a rectangle using user-input length and width

# inputs
len = float (input("Enter the length of the rectangle: "))
wid = float (input("Enter the width of the rectangle: "))

#input value being cm

# formula of rectangle
# Area = length * width
area = len * wid

# result
print(f"Area of the rectangle is {area} square cm")